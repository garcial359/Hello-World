public class Book {

    public String title2;
    public String author;

    public Book(String title2, String author) {
        this.title2 = title2;
        this.author = author;
    }

    public String getTitle2() {
        return title2;
    }

    public void setTitle2(String title2) {
        this.title2 = title2;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

}

